namespace Spryker\Zed\ShipmentTypeServicePointsRestApi\Business;

use Generated\Shared\Transfer\QuoteTransfer;
use Generated\Shared\Transfer\RestCheckoutRequestAttributesTransfer;
use Spryker\Zed\Kernel\Business\AbstractFacade;

/**
 * @method \Spryker\Zed\ShipmentTypeServicePointsRestApi\Business\ShipmentTypeServicePointsRestApiBusinessFactory getFactory()
 */
class ShipmentTypeServicePointsRestApiFacade extends AbstractFacade implements ShipmentTypeServicePointsRestApiFacadeInterface
{
    /**
     * {@inheritDoc}
     *
     * @api
     *
     * @param \Generated\Shared\Transfer\RestCheckoutRequestAttributesTransfer $restCheckoutRequestAttributesTransfer
     * @param \Generated\Shared\Transfer\QuoteTransfer $quoteTransfer
     *
     * @return \Generated\Shared\Transfer\QuoteTransfer
     */
    public function mapCustomerAddressDataToShippingAddresses(
        RestCheckoutRequestAttributesTransfer $restCheckoutRequestAttributesTransfer,
        QuoteTransfer $quoteTransfer
    ): QuoteTransfer {
        return $this->getFactory()
            ->createQuoteMapperStrategyResolver()
            ->resolve($restCheckoutRequestAttributesTransfer)
            ->mapCustomerAddressDataToShippingAddresses($restCheckoutRequestAttributesTransfer, $quoteTransfer);
    }
}
